class TestRalphProcessFailIntegration:
    """Integration tests for Ralph.process_signal with fail signal."""

    @pytest.fixture
    def ralph_with_tasks(self, tmp_path):
        """Create Ralph with a multi-task plan."""
        from src.ralph import Ralph

        plan_file = tmp_path / "IMPLEMENTATION_PLAN.md"
        plan_file.write_text("""# Implementation Plan: Integration Test

## Tasks

- [ ] **Health Check** (T001)
  Check system health
- [ ] **API Status** (T002)
  Check API status

---
*Auto-generated*
""")

        with patch("src.plan.IMPLEMENTATION_PLAN", plan_file):
            ralph = Ralph(project_path=str(tmp_path))
            ralph.load_plan()
            return ralph

    def test_integration_fail_signal_keeps_task(
        self, ralph_with_tasks, temp_signal_path
    ):
        """Full integration: fail signal -> retry same task."""
        from src.ralph import Ralph

        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_tasks

            # Step 1: Verify starting at T001
            task = ralph.get_current_task()
            assert task.id == "T001"

            # Step 2: Write fail signal to file
            with open(temp_signal_path, "w") as f:
                json.dump({"pass": False}, f)

            # Step 3: Ralph reads signal
            ralph.load_signal()
            assert ralph.signal.pass_result is False

            # Step 4: Ralph processes signal
            ralph.process_signal()

            # Step 5: Verify T001 is still current (retry)
            new_task = ralph.get_current_task()
            assert new_task.id == "T001", "Should retry same task after fail"

            # Step 6: Verify retry count was incremented
            assert new_task.retry_count == 1, "Retry count should be 1"

            # Step 7: Verify T001 is NOT marked done
            t001 = next((t for t in ralph.plan.tasks if t.id == "T001"), None)
            assert t001.status == "pending", "Task should still be pending"
        finally:
            Signal.SIGNAL_PATH = original_signal_path

    def test_integration_fail_signal_does_not_advance(
        self, ralph_with_tasks, temp_signal_path
    ):
        """Fail signal should NOT advance to next task."""
        from src.ralph import Ralph

        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_tasks

            # Write fail signal
            temp_signal_path.write_text('{"pass": false}')
            ralph.load_signal()
            ralph.process_signal()

            # Should still be on T001, NOT T002
            new_task = ralph.get_current_task()
            assert new_task.id == "T001", "Should NOT advance to T002 on fail"
        finally:
            Signal.SIGNAL_PATH = original_signal_path
