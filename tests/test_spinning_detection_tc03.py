    def test_spinning_signal_read_back_works(self, ralph_with_task, temp_signal_path):
        """Spinning signal can be read back using Signal.read()."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

