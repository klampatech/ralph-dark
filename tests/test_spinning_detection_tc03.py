class TestSpinningDetection:
    """Tests for TC-03: Spinning detection after 5 consecutive failures."""

    def test_fifth_failure_triggers_spinning_signal(
        self, ralph_with_task, temp_signal_path
    ):
        """When Ralph processes the 5th failure signal, spinning signal is written."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_task
            task = ralph.get_current_task()
            assert task.id == "T001"

            # Simulate 5 consecutive failures
            for retry_num in range(1, 6):
                # Write fail signal
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

                # After the 5th failure (retry_num == 5), spinning should be triggered
                if retry_num == 5:
                    # Verify spinning signal was written
                    assert temp_signal_path.exists()
                    content = json.loads(temp_signal_path.read_text())
                    assert content["spinning"] is True
                    assert content["task"] == task.title
        finally:
            Signal.SIGNAL_PATH = original_signal_path

    def test_spinning_signal_written_at_exactly_five_failures(
        self, ralph_with_task, temp_signal_path
    ):
        """Spinning signal is written when retry count reaches exactly 5."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_task

            # Fail 4 times - no spinning yet
            for _ in range(4):
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

            # At 4 failures, should NOT have spinning signal yet
            content_4 = temp_signal_path.read_text()
            if content_4:
                parsed = json.loads(content_4)
                assert "spinning" not in parsed or parsed.get("spinning") is not True

            # 5th failure
            temp_signal_path.write_text('{"pass": false}')
            ralph.load_signal()
            ralph.process_signal()

            # Now spinning signal should exist
            content = json.loads(temp_signal_path.read_text())
            assert content.get("spinning") is True
        finally:
            Signal.SIGNAL_PATH = original_signal_path

    def test_spinning_signal_contains_task_description(
        self, ralph_with_task, temp_signal_path
    ):
        """Spinning signal contains the task's title/description."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_task
            task = ralph.get_current_task()
            expected_task_name = task.title

            # Simulate 5 failures
            for _ in range(5):
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

            content = json.loads(temp_signal_path.read_text())
            assert content["task"] == expected_task_name
            assert "Implement Feature X" in content["task"]
        finally:
            Signal.SIGNAL_PATH = original_signal_path

    def test_spinning_signal_has_correct_structure(self, ralph_with_task, temp_signal_path):
        """Spinning signal has exactly { "spinning": true, "task": "..." }."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_task

            # Trigger spinning with 5 failures
            for _ in range(5):
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

            content = json.loads(temp_signal_path.read_text())

            # Should have exactly these keys
            assert set(content.keys()) == {"spinning", "task"}
            assert content["spinning"] is True
            assert isinstance(content["task"], str)

            # Should NOT have pass field
            assert "pass" not in content
            assert "done" not in content
        finally:
            Signal.SIGNAL_PATH = original_signal_path


class TestSpinningDetectionIntegration:
    """Integration tests for full spinning detection flow."""

    def test_spinning_detected_on_specific_task_not_others(
        self, ralph_multitask, temp_signal_path
    ):
        """Spinning detection applies to the specific task being retried."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_multitask

            # Should start at T002 (first pending task)
            task = ralph.get_current_task()
            assert task.id == "T002"

            # Simulate 5 failures on T002
            for _ in range(5):
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

            # Spinning signal should reference T002's description
            content = json.loads(temp_signal_path.read_text())
            assert "Failing Task" in content["task"]
            assert "Completed Task" not in content["task"]
            assert "Future Task" not in content["task"]
        finally:
            Signal.SIGNAL_PATH = original_signal_path

    def test_spinning_signal_not_leaked_to_harness(
        self, ralph_with_task, temp_signal_path
    ):
        """Spinning signal doesn't leak which scenario failed or error details."""
        original_signal_path = Signal.SIGNAL_PATH
        Signal.SIGNAL_PATH = temp_signal_path

        try:
            ralph = ralph_with_task

            # Trigger spinning
            for _ in range(5):
                temp_signal_path.write_text('{"pass": false}')
                ralph.load_signal()
                ralph.process_signal()

            content_str = temp_signal_path.read_text()
            content = json.loads(content_str)

            # Should not contain error details
            forbidden = ["error", "reason", "detail", "assertion", "failed", "scenario", "T002"]
            for word in forbidden:
                # Allow the word in task name only if it's part of "Failing Task"
                if word in content_str.lower():
                    assert word in content.get("task", "")

            # Only spinning and task should be present
            assert set(content.keys()) == {"spinning", "task"}
        finally:
            Signal.SIGNAL_PATH = original_signal_path


